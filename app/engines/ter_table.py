def get_ter_rate(gross: float, ptkp_status: str) -> float:
    if ptkp_status not in PTKP:
        raise ValueError("Invalid PTKP status")

    category = PTKP[ptkp_status]["ter"]
    table = TER_TABLE[category]

    for lower, upper, rate in table:
        if lower <= gross <= upper:
            return rate

    return 0.0